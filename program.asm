; Пример программы на мнемоническом языке
; Инициализация данных в памяти

ORG 0x0100      ; Установка адреса для данных
DS 42.0         ; Вещественное число 42.0

ORG 0x0104
DS 3.14         ; Вещественное число 3.14

ORG 0x0108
DS 0xFFFFFFFF   ; Целое число -1

ORG 0x010C
DS 0x0F0F0F0F   ; Целое число 0x0F0F0F0F

ORG 0x0110
DS 0x00FF00FF   ; Целое число 0x00FF00FF

ORG 0x0114
DS 4            ; Целое число 4

; Код программы
ORG 0x0000      ; Возврат к началу для кода

; Загрузка значений в стек и выполнение операций
PUSH [0x0100]   ; Загрузка 42.0 в стек
PUSH [0x0104]   ; Загрузка 3.14 в стек
ADD_F           ; Сложение вещественных чисел
POP [0x0118]    ; Сохранение результата

PUSH [0x0108]   ; Загрузка -1 в стек
PUSH [0x010C]   ; Загрузка 0x0F0F0F0F в стек
AND             ; Логическое И
POP [0x011C]    ; Сохранение результата

PUSH [0x0110]   ; Загрузка 0x00FF00FF в стек
PUSH [0x0114]   ; Загрузка 4 в стек
SHL             ; Сдвиг влево на 4 бита
POP [0x0120]    ; Сохранение результата

; Условный переход
PUSH [0x0118]   ; Загрузка результата сложения
PUSH [0x0100]   ; Загрузка 42.0
CMP_F           ; Сравнение вещественных чисел
JZ equal        ; Переход если равны

; Если не равны
PUSH 1
OUT             ; Вывод 1
JMP end

equal: PUSH 0   ; Метка для перехода с инструкцией
OUT             ; Вывод 0

end: RET        ; Метка с инструкцией RET